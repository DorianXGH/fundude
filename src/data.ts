import { DIMENSIONS } from "./display";

function asScreen(str: string): Uint8Array {
  const nums = str
    .trim()
    .split(/\s+/)
    .map(line => line.padEnd(DIMENSIONS[0], "0"))
    .join("")
    .split("")
    .map(Number);
  const ret = new Uint8Array(DIMENSIONS[0] * DIMENSIONS[1]);
  ret.set(nums);
  return ret;
}

